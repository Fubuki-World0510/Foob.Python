# Các ký tự nằm trong "" '' là chuỗi
print("Đây là chuỗi", 'Đây là chuỗi')

# Ký tự đặc biệt trong chuỗi
print("Ký tự xuống dòng\nĐã xuống")
print("Tạo chuông\a")
print("Tạo ra khoảng trắng giữa nó\tđúng không?")
print("Xóa bỏ khoảng trắng trước\b")
print("Ký tự đóng ngoặc\"trong ngoặc\"")

# Gán dữ liệu trong chuỗi
em = "Etou"
print("%s" %(em))

# Index dữ liệu chuỗi
index = "Shirakami"
print(index[1]) # Out: h
print(index[-1]) # Out: i
print(index[5:9]) # Out: kami