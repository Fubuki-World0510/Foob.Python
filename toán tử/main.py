# Toán tử logic
## AND - "và": dùng trong so sánh kiểu như so sánh a = b và a=c, nếu đáp ứng 2 điều kiện a=b và a=c thì true
## OR - "hoặc": dùng trong so sánh kiểu như so sánh a = b hoặc a=c, nếu đáp ứng 1 trong 2 điều kiện a=b hoặc a=c thì true
## NOT - "không": dùng trong so sánh kiểu như so sánh phủ định a và b, nếu a không bằng b thì trả về true

